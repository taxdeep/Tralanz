#!/usr/bin/env node
// Build a single SVG sprite from a fixed allowlist of Tabler outline icons.
// Output: backend/src/Citus.Ui.Shared/Icons/tabler-sprite.svg
// Output: backend/src/Citus.Ui.Shared/Icons/IconName.cs (strongly-typed enum)
//
// Run: pnpm run icons:sync
// To add an icon: append its kebab-case Tabler name below, then re-run.

import { readFileSync, writeFileSync, existsSync, mkdirSync } from "node:fs";
import { dirname, resolve } from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const REPO_ROOT = resolve(__dirname, "..", "..", "..");
const TABLER_DIR = resolve(__dirname, "..", "node_modules", "@tabler", "icons", "icons", "outline");
const OUT_DIR = resolve(REPO_ROOT, "backend", "src", "Citus.Ui.Shared", "Icons");
const SPRITE_PATH = resolve(OUT_DIR, "tabler-sprite.svg");
const ENUM_PATH = resolve(OUT_DIR, "IconName.cs");

// Allowlist — keep alphabetised. Names map to outline/<name>.svg in @tabler/icons.
const ICONS = [
  "alert-circle",
  "alert-triangle",
  "arrow-left",
  "arrow-right",
  "building",
  "building-bank",
  "calendar",
  "cash",
  "check",
  "chevron-down",
  "chevron-left",
  "chevron-right",
  "chevron-up",
  "circle-check",
  "clock",
  "coin",
  "dashboard",
  "dots",
  "download",
  "eye",
  "file",
  "file-invoice",
  "file-text",
  "filter",
  "home",
  "info-circle",
  "logout",
  "menu-2",
  "moon",
  "pencil",
  "plus",
  "receipt",
  "refresh",
  "report",
  "search",
  "settings",
  "shield-lock",
  "sun",
  "trash",
  "user",
  "users",
  "wallet",
  "world",
  "x"
];

if (!existsSync(TABLER_DIR)) {
  console.error(`Tabler source not found at ${TABLER_DIR}.`);
  console.error("Run `pnpm install` inside backend/frontend first.");
  process.exit(1);
}

mkdirSync(OUT_DIR, { recursive: true });

const symbols = [];
const enumLines = [];
let stripped = 0;

for (const name of ICONS) {
  const file = resolve(TABLER_DIR, `${name}.svg`);
  if (!existsSync(file)) {
    console.error(`  miss: ${name}.svg not in @tabler/icons`);
    process.exit(2);
  }
  const raw = readFileSync(file, "utf8");
  const inner = raw
    .replace(/<\?xml[^?]*\?>/g, "")
    .replace(/<!--[\s\S]*?-->/g, "")
    .replace(/<svg\b[^>]*>/, "")
    .replace(/<\/svg>\s*$/, "")
    .trim();
  if (!inner) {
    console.error(`  empty: ${name}.svg has no inner content`);
    process.exit(3);
  }
  symbols.push(`  <symbol id="ti-${name}" viewBox="0 0 24 24">${inner}</symbol>`);
  enumLines.push(`        ${kebabToPascal(name)},`);
  stripped++;
}

const sprite =
  '<?xml version="1.0" encoding="UTF-8"?>\n' +
  '<svg xmlns="http://www.w3.org/2000/svg" style="display:none">\n' +
  symbols.join("\n") +
  "\n</svg>\n";

writeFileSync(SPRITE_PATH, sprite, "utf8");

const enumSource =
  "// <auto-generated>\n" +
  "//   Generated by backend/frontend/scripts/sync-tabler-icons.mjs\n" +
  "//   Do not edit by hand — update the allowlist and re-run `pnpm run icons:sync`.\n" +
  "// </auto-generated>\n" +
  "namespace Citus.Ui.Shared.Icons;\n\n" +
  "/// <summary>\n" +
  "/// Strongly-typed Tabler icon names available in tabler-sprite.svg.\n" +
  "/// </summary>\n" +
  "public enum IconName\n" +
  "{\n" +
  enumLines.join("\n") +
  "\n}\n";

writeFileSync(ENUM_PATH, enumSource, "utf8");

const idMapPath = resolve(OUT_DIR, "IconNameMap.g.cs");
const mapEntries = ICONS.map(name => `        { IconName.${kebabToPascal(name)}, "ti-${name}" }`).join(",\n");
const mapSource =
  "// <auto-generated>\n" +
  "//   Generated by backend/frontend/scripts/sync-tabler-icons.mjs\n" +
  "// </auto-generated>\n" +
  "using System.Collections.Generic;\n\n" +
  "namespace Citus.Ui.Shared.Icons;\n\n" +
  "internal static class IconNameMap\n" +
  "{\n" +
  "    public static readonly IReadOnlyDictionary<IconName, string> SpriteIds = new Dictionary<IconName, string>\n" +
  "    {\n" +
  mapEntries +
  "\n    };\n" +
  "}\n";

writeFileSync(idMapPath, mapSource, "utf8");

console.log(`tabler: synced ${stripped} icons → ${SPRITE_PATH}`);
console.log(`tabler: wrote enum  → ${ENUM_PATH}`);
console.log(`tabler: wrote map   → ${idMapPath}`);

function kebabToPascal(value) {
  return value
    .split("-")
    .map(part => part.charAt(0).toUpperCase() + part.slice(1))
    .join("");
}
